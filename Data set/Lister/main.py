# Program hvor lister benyttes
# String list
mylist = ["Lasse", "Anders", "Peter", "Lasse"]
print("String liste")
print(mylist)
print("Kommadoen len tæller hvormange emner der i listen")
print(f"Hvormange elementer er i listen: {len(mylist)}")  # len tæller hvor mange elementer
print(f"Hvormange bogstaver er i første element: {len(mylist[0])}")  # Tæller hvormange bogstaver der i navnet
print()
